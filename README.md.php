# Photonic Flickr Only

Versión minimalista del plugin Photonic, enfocada exclusivamente en galerías de Flickr.

## Características

- ✅ Soporte para `photosets` de Flickr
- ✅ Lightbox moderno con GLightbox
- ✅ Shortcode limpio: `[photonic_flickr user_id="..." photoset_id="..."]`
- ✅ Panel de configuración para API Key

## Instalación

1. Subir carpeta `photonic-flickr` a `/wp-content/plugins/`
2. Activar desde el panel de WordPress
3. Ir a Ajustes → Photonic Flickr y agregar tu API Key

